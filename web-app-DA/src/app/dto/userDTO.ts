export class userDTO {
  id:number;
  name:string;
  username:string;
  email:string;
  avatar : string;

  constructor(id: number, name: string, username: string, email: string, avatar: string) {
    this.id = id;
    this.name = name;
    this.username = username;
    this.email = email;
    this.avatar = avatar;
  }
}
